def node_meta(d):
    """Per-surface-node metadata: x/c, side (+1 upper / -1 lower), regime
    (laminar/turbulent/pin), region label, the closure-floor mirrors and
    the Q2 aggregate null energy (filled by q2 before q4)."""
    pack, st = d["pack"], d["st"]
    case = pack.case
    x_c = pack.sm.xyz[:, 0] / pack.cfg.chord
    side = np.asarray(case.stations.side_node, dtype=int)
    turb = np.asarray(case.turbulent_flags, dtype=int)
    inflow = np.asarray(st["inflow_mask"], dtype=bool)
    regime = np.where(inflow, "inflow_pin",
                      np.where(turb == 1, "turbulent", "laminar"))
    region = np.where(x_c <= pack.cfg.inflow_band_x, "le_inflow",
                      np.where(x_c <= pack.cfg.le_band_x, "le_band",
                               np.where(x_c >= 0.9, "te_band", "mid")))
    return {"x_c": x_c, "side": side, "turb": turb, "inflow": inflow,
            "regime": regime, "region": region}


def null_energy(d):
    """Q2: aggregate per-node energy of the top-N_NULL smallest-sigma
    RIGHT singular vectors (each vector's energy normalized to 1 first),
    plus the per-variable energy fractions (mean over the same vectors)."""
    n_s = d["pack"].n_s
    V = d["Vh"][-N_NULL:].T.reshape(n_s, 6, N_NULL)  # (node, var, rank)
    e_node = np.mean(np.sum(V ** 2, axis=1), axis=1)          # (node,)
    e_var = np.mean(np.sum(V ** 2, axis=0), axis=1)           # (6,)
    assert abs(float(np.sum(e_var)) - 1.0) < 1e-9, "e_var must sum to 1"
    return e_node, e_var


def _counts(s):
    """Near-null counts below the pre-registered relative thresholds."""
    return {f"{r:.0e}": int(np.sum(s < r * s[0])) for r in SIG_RELS}


