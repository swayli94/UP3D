def _write_summary():
    """summary.csv: one row per (question, states), RECORDED. Merge-written
    after every record: fresh rows replace the same (question, states)
    pair, rows of phases not re-run are kept (the v5_tight_coupling
    _merge_tagged discipline) -- a late crash loses only the phases that
    did not complete."""
    rows = {}
    path = os.path.join(RESULTS, "summary.csv")
    if os.path.exists(path):
        with open(path) as f:
            lines = [ln.rstrip("\n") for ln in f if ln.strip()]
        for ln in lines[1:]:
            flds = ln.split(",", 2)
            if len(flds) >= 2:
                rows[(flds[0], flds[1])] = ln
    for t in SUMMARY:
        rows[(t[0], t[1])] = ",".join(str(x) for x in t)
    order = {f"Q{i}": i for i in range(1, 8)}
    out = sorted(rows.values(),
                 key=lambda ln: (order.get(ln.split(",", 1)[0], 99),
                                 ln.split(",", 2)[1]))
    _write_lines("summary.csv",
                 "question,states,key_result,artifacts,verdict", out)


