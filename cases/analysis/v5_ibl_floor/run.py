def build_loose_state(level):
    """S1/S2: the loose loop's converged state, regenerated with the
    committed GV3.1 recipe (../v5_tight_coupling/run.py phase 1, verbatim:
    picard driver, omega=1.0, n_outer_max=10, tol_ds=1e-3), wiring guard
    included (converged + |dcl_k0| <= 1e-8 vs the committed GV3.1 history,
    read -- never recomputed)."""
    import csv

    print(f"--- [{level}] loose-loop regeneration (committed GV3.1 recipe, "
          "the GV5.1 amended seed) ---", flush=True)
    mc, wc = cut_wake(read_mesh(os.path.join(NACA_DIR, f"{level}.msh")))
    cfg = CouplingConfig(re_chord=RE, m_inf=M_INF, alpha_deg=ALPHA)
    case = build_airfoil_case(
        mc.nodes, mc.elements, mc.boundary_faces["wall"], cfg)
    dz = float(np.ptp(mc.nodes[:, 2]))
    s_ref = 1.0 * dz

    from pyfp3d.post.surface import wall_force_coefficients

    def probe(phi, gamma, k):
        f = wall_force_coefficients(
            mc.nodes, mc.elements, mc.boundary_faces["wall"], phi,
            alpha_deg=ALPHA, s_ref=s_ref, m_inf=M_INF)
        return {"cl": f["cl"], "cd_p": f["cd_pressure"]}

    t0 = time.perf_counter()
    driver = make_picard_lifting_driver(mc, wc, M_INF, ALPHA)
    res = run_loose_coupling(driver, case, cfg, probe=probe)
    wall = time.perf_counter() - t0
    with open(os.path.join(V3_RESULTS, f"gv3_1_history_{level}.csv")) as f:
        loose_hist = list(csv.DictReader(f))
    d_k0 = abs(float(res.history[0]["cl"]) - float(loose_hist[0]["cl"]))
    guard_ok = bool(res.converged) and d_k0 <= RECIPE_TOL_K0
    print(f"    loose regen in {wall:.0f}s: converged={res.converged} "
          f"n_outer={res.n_outer} cl={float(res.history[-1]['cl']):.8f} "
          f"| wiring guard: dcl_k0={d_k0:.3e} (<= {RECIPE_TOL_K0:.0e}) "
          f"-> {'OK' if guard_ok else 'RECIPE ERROR'}", flush=True)
    if not guard_ok:
        raise RuntimeError(
            f"[{level}] loose-regen wiring guard failed (converged="
            f"{res.converged}, dcl_k0={d_k0:.3e}) -- recipe error, STOP")
    build_lc = _load_gv51_recipe()
    st = build_lc(mc, wc, cfg, case, res)
    st["loose_res"] = res
    st["level"] = level
    return st


def build_pack_and_svd(tag, st):
    """The tight pack at the state + the dense SVD of J_BL,BL at
    pack.x_base() (the pre-registered operator). Returns
    dict(tag, st, pack, J, U_svd, s, Vh)."""
    t0 = time.perf_counter()
    pack = td.build_tight_pack(st, UPWIND_C, M_CRIT, M_CAP, RHO_FLOOR)
    bl = td.augmented_jacobian_blocks(pack, pack.x_base())
    J = bl["J_blbl"].toarray()
    U_svd, s, Vh = np.linalg.svd(J)
    wall = time.perf_counter() - t0
    print(f"  [{tag}] pack + dense SVD of J_BL,BL ({J.shape[0]}^2) in "
          f"{wall:.0f}s: sigma_max={s[0]:.6e} sigma_min={s[-1]:.6e} "
          f"cond={s[0] / s[-1]:.3e}", flush=True)
    return {"tag": tag, "st": st, "pack": pack, "J": J,
            "U_svd": U_svd, "s": s, "Vh": Vh}


