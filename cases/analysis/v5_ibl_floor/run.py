def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--states", nargs="+", default=["s1", "s2", "s3"],
                    choices=["s1", "s2", "s3"],
                    help="states to diagnose (default: all three)")
    ap.add_argument("--phases", nargs="+",
                    default=["q1", "q2", "q3", "q4", "q5", "q6", "q7"],
                    choices=["q1", "q2", "q3", "q4", "q5", "q6", "q7"],
                    help="questions to run (default: all; q6 implies q7 -- "
                         "the Q6 base re-solve IS the Q7 run). Partial "
                         "runs merge summary.csv rows by (question, "
                         "states); the default full run regenerates "
                         "every artifact from scratch.")
    args = ap.parse_args()
    t_all = time.perf_counter()
    phases = set(args.phases)
    if "q6" in phases:
        phases.add("q7")

    states = {}
    if "s3" in args.states:
        print("=== S3: coarse k=1 fixture (tests/v5_state.py) ===",
              flush=True)
        states["s3"] = build_pack_and_svd("s3", build_k1_state(
            build_naca_case()))
    if "s1" in args.states:
        print("=== S1: coarse loose-converged state ===", flush=True)
        states["s1"] = build_pack_and_svd("s1", build_loose_state("coarse"))
    if "s2" in args.states:
        print("=== S2: medium loose-converged state (GV5.1 section-4 "
              "trajectory-scatter caveat on every number) ===", flush=True)
        states["s2"] = build_pack_and_svd("s2", build_loose_state("medium"))

    if "q1" in phases:
        q1(states)

    # Q2 at S1 always; at S2 iff the near-null cluster persists (Q1)
    if "q2" in phases:
        if "s1" in states:
            q2("s1", states["s1"])
        if "s2" in states:
            cnt_s2 = _counts(states["s2"]["s"])["1e-06"]
            persist = cnt_s2 >= N_NULL
            print(f"  Q2[s2]: near-null cluster persists at S2 = "
                  f"{persist} (count(1e-6)={cnt_s2})", flush=True)
            if persist:
                q2("s2", states["s2"])

    if "q3" in phases:
        q3(states)

    if "q4" in phases:
        for tag in ("s1", "s2"):
            if tag in states:
                q4(tag, states[tag])

    if "q5" in phases:
        figs = {}
        for tag in ("s1", "s2"):
            if tag in states:
                d = states[tag]
                figs[tag] = (q5(tag, d), d["s"])
        if figs:
            q5_pngs(figs)

    if "q7" in phases and "s1" in states:
        base = q7(states["s1"])   # the x1 re-solve doubles as the Q6 base
        if "q6" in phases:
            q6(states["s1"], base)

    _write_summary()
    print(f"\nV5 IBL-floor diagnosis done in "
          f"{time.perf_counter() - t_all:.0f}s: {len(SUMMARY)} RECORDED "
          "rows (no pass/fail bands)", flush=True)


if __name__ == "__main__":
    main()
