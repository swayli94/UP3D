def q7(d):
    print("=== Q7 pseudo-time stall anatomy [s1] (standalone IBL3Solver "
          "from S1, verbose controller trace) ===", flush=True)
    st = d["st"]
    cfg = st["cfg"]
    solver = st["solver"]
    U0 = np.ascontiguousarray(st["U"], dtype=np.float64)
    buf = io.StringIO()
    t0 = time.perf_counter()
    with contextlib.redirect_stdout(buf):
        U, info = solver.solve(U0, tol=cfg.ibl_tol,
                               max_iter=cfg.ibl_max_iter, verbose=True)
    wall = time.perf_counter() - t0
    lines, events = [], []
    cur_it = None
    for ln in buf.getvalue().splitlines():
        m = re.match(r"\[ibl3\] it=(\d+) cfl=([\d.eE+-]+) \|R\|inf="
                     r"([\d.eE+-]+)", ln)
        if m:
            cur_it = int(m.group(1))
            events.append(("iter", cur_it, float(m.group(2)),
                           float(m.group(3))))
        elif "step rejected" in ln:
            m2 = re.search(r"cfl <- ([\d.eE+-]+)", ln)
            events.append(("reject", cur_it, float(m2.group(1)), ""))
    for kind, it, cfl, r in events:
        lines.append(f"{kind},{it},{cfl:.6e},{r if r == '' else f'{r:.6e}'}")
    hist = info["residual_history"]
    lines.append(",".join([
        "final", str(info["n_iter"]), f"{info['cfl_final']:.6e}",
        f"{info['final_residual']:.6e}"]))
    _write_lines("pseudotime_stall.csv", "record,iter,cfl,r_inf", lines)
    n_rej = sum(1 for k, *_ in events if k == "reject")
    cfl_min = 1.0e-3  # cfl0 (=1.0) * 1e-3, ibl3.py:1476
    plateau = (hist[-1] / hist[0]) if hist and hist[0] > 0 else float("nan")
    tail = (f"last 10 iters: {hist[-10]:.3e} -> {hist[-1]:.3e}"
            if len(hist) >= 10 else "n/a")
    bottomed = info["cfl_final"] <= 1.5 * cfl_min and n_rej > 0
    _record(
        "Q7", "s1",
        f"converged={info['converged']} n_iter={info['n_iter']} "
        f"rejections={n_rej} cfl_final={info['cfl_final']:.3e} "
        f"(cfl_min={cfl_min:.0e}); r_inf {hist[0]:.3e} -> "
        f"{hist[-1]:.3e} (ratio {plateau:.4f}); {tail}; "
        f"{'controller BOTTOMED OUT (cfl pinned at cfl_min; steps rejected; residual frozen at the floor)' if bottomed else 'residual plateaus at FULL step (formulation floor signature)'}; "
        f"wall={wall:.0f}s",
        "pseudotime_stall.csv")
    return U, info, np.linalg.svd(
        solver.residual_jacobian(U)[1].toarray(), compute_uv=False)


