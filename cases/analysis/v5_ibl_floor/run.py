def q3(states):
    print("=== Q3 row/column equilibration: scaling artifact vs true "
          "rank deficiency ===", flush=True)
    lines = []
    for tag, d in states.items():
        J = d["J"]
        s0 = d["s"]
        rn = np.linalg.norm(J, axis=1)
        J1 = J * (1.0 / np.maximum(rn, 1e-300))[:, None]
        cn = np.linalg.norm(J1, axis=0)
        J2 = J1 * (1.0 / np.maximum(cn, 1e-300))[None, :]
        s1v = np.linalg.svd(J2, compute_uv=False)
        c0 = _counts(s0)
        c1 = _counts(s1v)
        lines.append(",".join([
            tag, f"{s0[0] / s0[-1]:.6e}", f"{s1v[0] / s1v[-1]:.6e}",
            f"{rn.max() / rn.min():.6e}", f"{cn.max() / cn.min():.6e}",
            str(c0["1e-06"]), str(c1["1e-06"]),
            str(c0["1e-08"]), str(c1["1e-08"]),
            str(c0["1e-10"]), str(c1["1e-10"])]))
        _record(
            "Q3", tag,
            f"cond raw={s0[0] / s0[-1]:.3e} -> equilibrated="
            f"{s1v[0] / s1v[-1]:.3e}; row-norm range={rn.max() / rn.min():.2e}"
            f" col-norm range={cn.max() / cn.min():.2e}; near-null "
            f"(<1e-6*smax) raw={c0['1e-06']} -> scaled={c1['1e-06']}",
            "scaling.csv")
    _write_lines(
        "scaling.csv",
        "state,cond_raw,cond_equilibrated,row_norm_range,col_norm_range,"
        "n_lt_1e-6_raw,n_lt_1e-6_scaled,n_lt_1e-8_raw,n_lt_1e-8_scaled,"
        "n_lt_1e-10_raw,n_lt_1e-10_scaled", lines)


