def _resolve_with_eps(st, f_eps, f_eps_s, U0):
    """A fresh IBL3Solver identical to the S1 one except the artificial
    diffusion coefficients (the pre-registered Q6(a) interface probe --
    eps_diff/eps_diff_s are constructor kwargs, ibl3.py:1182-1184),
    solved from the SAME seed U0 = S1's U."""
    cfg = st["cfg"]
    solver = IBL3Solver(
        st["sm"], st["ue_surf"], st["rho_e"], st["mu"], st["mach_e"],
        st["case"].turbulent_flags, st["inflow_mask"], st["inflow_state"],
        eps_diff=cfg.eps_diff * f_eps, eps_diff_s=cfg.eps_diff_s * f_eps_s)
    U, info = solver.solve(U0, tol=cfg.ibl_tol, max_iter=cfg.ibl_max_iter)
    _, J = solver.residual_jacobian(U)
    s = np.linalg.svd(J.toarray(), compute_uv=False)
    return U, info, s


def q6(d, base):
    print("=== Q6 formulation sensitivity probes [s1] ===", flush=True)
    st = d["st"]
    cfg = st["cfg"]
    U0 = np.ascontiguousarray(st["U"], dtype=np.float64)
    base_U, base_info, base_s = base
    base_floor = base_info["final_residual"]
    base_cnt = _counts(base_s)
    lines = [",".join([
        "eps_base(x1)", "1.0", "1.0", f"{cfg.eps_diff:.4g}",
        f"{cfg.eps_diff_s:.4g}", str(base_info["converged"]),
        str(base_info["n_iter"]), f"{base_floor:.6e}", "1.0",
        str(base_cnt["1e-06"]), str(base_cnt["1e-08"]),
        f"{base_s[0] / base_s[-1]:.6e}",
        "re-solve from S1 U at the pack eps (the Q7 run)"])]
    for f_e, f_es, lab in ((0.5, 1.0, "eps_diff x0.5"),
                           (2.0, 1.0, "eps_diff x2"),
                           (0.5, 0.5, "both x0.5 (extra)"),
                           (2.0, 2.0, "both x2 (extra)")):
        t0 = time.perf_counter()
        _, info, s = _resolve_with_eps(st, f_e, f_es, U0)
        cnt = _counts(s)
        wall = time.perf_counter() - t0
        lines.append(",".join([
            lab, f"{f_e:.2f}", f"{f_es:.2f}",
            f"{cfg.eps_diff * f_e:.4g}", f"{cfg.eps_diff_s * f_es:.4g}",
            str(info["converged"]), str(info["n_iter"]),
            f"{info['final_residual']:.6e}",
            f"{info['final_residual'] / base_floor:.4f}",
            str(cnt["1e-06"]), str(cnt["1e-08"]),
            f"{s[0] / s[-1]:.6e}", f"wall={wall:.0f}s"]))
        print(f"  Q6 {lab}: converged={info['converged']} "
              f"n_iter={info['n_iter']} floor={info['final_residual']:.3e} "
              f"(x{info['final_residual'] / base_floor:.3f} vs base) "
              f"near-null(1e-6)={cnt['1e-06']} ({wall:.0f}s)", flush=True)
    # Q6(b): DELTA_MIN probe -- DEFERRED per the pre-registration fallback
    # clause + the 2026-07-24 user instruction ("no hard source edits"):
    # DELTA_MIN/RE_D_MIN are module constants baked into the
    # @njit(cache=True) closures (closures.py:77-78, consumed inside
    # closure_node at :490-507); they are exposed through NEITHER the
    # closure_all signature NOR the IBL3Solver constructor, and numba
    # freezes globals at compile time, so the pre-registered "logged
    # local edit reverted in the same run" would silently no-op against
    # the cached compilation (an unclean probe). Substitute evidence:
    # the Q4 active set (if empty, the DELTA_MIN sensitivity is
    # identically zero at S1 by construction).
    lines.append(",".join([
        "DELTA_MIN probe", "", "", "", "", "", "", "", "", "", "", "",
        "DEFERRED (not cleanly probeable via the interface; numba-baked "
        "module constant; Q4 active set is the substitute evidence)"]))
    _write_lines(
        "sensitivity_probes.csv",
        "probe,factor_eps_diff,factor_eps_diff_s,eps_diff,eps_diff_s,"
        "converged,n_iter,final_residual,floor_ratio_vs_base,"
        "n_sigma_lt_1e-6,n_sigma_lt_1e-8,cond,note", lines)
    _record("Q6", "s1",
            f"(a) eps_diff probes done (see sensitivity_probes.csv; base "
            f"floor={base_floor:.3e} near-null(1e-6)={base_cnt['1e-06']}); "
            "(b) DELTA_MIN DEFERRED -- numba-baked module constant with no "
            "interface exposure; Q4 active set substitutes",
            "sensitivity_probes.csv")


