def q4(tag, d):
    """Active set of the closures.py state floors, mirrored exactly from
    closure_node (closures.py:490-507): DELTA_MIN active iff delta <
    1e-8; re_d = rho*q*max(delta, DELTA_MIN)/mu; RE_D_MIN active iff
    re_d < 1e-3. Edge data = the pack base (the pre-registered frozen
    semantics, pack.q0/rho_e0/mu). The remaining min/max clamps in
    closures.py are division guards (1e-30/1e-12 magnitudes, never active
    at a physical state) plus the inner ctau-solve step clamp (not a
    state map) -- noted, not active-set material."""
    print(f"=== Q4 closure-floor active set [{tag}] ===", flush=True)
    pack = d["pack"]
    meta = node_meta(d)
    U = pack.U0
    delta = U[:, 0]
    delta_f = np.maximum(delta, DELTA_MIN)
    re_d = pack.rho_e0 * pack.q0 * delta_f / pack.mu
    a_d = delta < DELTA_MIN
    a_r = re_d < RE_D_MIN
    e_null = d.get("e_node_null", np.zeros(pack.n_s))
    lines = []
    for i in range(pack.n_s):
        if a_d[i] or a_r[i]:
            lines.append(",".join([
                str(i), f"{meta['x_c'][i]:.6f}", f"{meta['side'][i]:d}",
                meta["regime"][i], meta["region"][i],
                f"{delta[i]:.6e}", f"{re_d[i]:.6e}",
                str(bool(a_d[i])), str(bool(a_r[i])),
                f"{e_null[i]:.8e}"]))
    _write_lines(
        f"closure_floor_active_{tag}.csv",
        "node,x_c,side,regime,region,delta,re_d,delta_min_active,"
        "re_d_min_active,null_energy", lines)
    # overlap with the Q2 null support (vacuous when the set is empty)
    n_act = int(np.sum(a_d | a_r))
    if n_act:
        mass = float(np.sum(e_null[a_d | a_r]))
        overlap = f"null-energy mass on active nodes={mass:.4f}"
    else:
        overlap = ("active set EMPTY -> floor-active exact-null-direction "
                   "hypothesis not operative at this state; the DELTA_MIN "
                   "sensitivity is identically zero here (Q6(b) substitute)")
    _record(
        "Q4", tag,
        f"DELTA_MIN-active={int(np.sum(a_d))} RE_D_MIN-active="
        f"{int(np.sum(a_r))} of {pack.n_s} nodes "
        f"(min delta={delta.min():.3e} min re_d={re_d.min():.3e}); "
        + overlap,
        f"closure_floor_active_{tag}.csv")


